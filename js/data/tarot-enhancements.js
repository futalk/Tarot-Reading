export function getCardEnhancement(cardName, isReversed = false) {
    const enhancement = tarotEnhancements[cardName];
    if (!enhancement) {
        return {
            deepMeaning: isReversed ? 
                '这张牌的逆位提醒你需要重新审视当前的状况，可能存在阻碍或需要调整的地方。' :
                '这张牌代表着当前能量的核心主题，深入理解它的含义将为你带来重要的洞见。',
            actionAdvice: isReversed ? [
                '反思当前的挑战，识别需要改变的模式',
                '寻求支持和指引，不要独自面对困难',
                '采取小步骤逐步改善，不要急于求成',
                '保持耐心和信心，转变需要时间'
            ] : [
                '把握当前的机会，积极采取行动',
                '相信你的直觉和内在智慧',
                '保持开放和灵活，适应新的可能性',
                '与他人分享你的洞见和经验'
            ]
        };
    }
    
    return {
        deepMeaning: isReversed ? enhancement.deepMeaning.reversed : enhancement.deepMeaning.upright,
        actionAdvice: isReversed ? enhancement.actionAdvice.reversed : enhancement.actionAdvice.upright
    };
}

