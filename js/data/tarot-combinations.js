/**
 * 获取所有组合的统计信息
 * @returns {Object} - 统计信息
 */
export function getCombinationStats() {
    const total = Object.keys(tarotCombinations).length;
    const themes = {};
    const contexts = {};
    
    for (const combo of Object.values(tarotCombinations)) {
        // 统计主题
        if (combo.theme) {
            themes[combo.theme] = (themes[combo.theme] || 0) + 1;
        }
        
        // 统计情境
        if (combo.context) {
            combo.context.forEach(ctx => {
                contexts[ctx] = (contexts[ctx] || 0) + 1;
            });
        }
    }
    
    return {
        total,
        themes: Object.keys(themes).length,
        contexts: Object.keys(contexts).length,
        topThemes: Object.entries(themes)
            .sort((a, b) => b[1] - a[1])
            .slice(0, 10)
            .map(([theme, count]) => ({ theme, count })),
        topContexts: Object.entries(contexts)
            .sort((a, b) => b[1] - a[1])
            .slice(0, 10)
            .map(([context, count]) => ({ context, count }))
    };
}

